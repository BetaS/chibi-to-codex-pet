export const MESSAGE_CATALOGS: Readonly<Record<AppLocale, TranslationCatalog>> = {
  en,
  ja,
  ko,
  'zh-CN': zhCN,
}

export function translateMessage(
  locale: AppLocale,
  key: MessageKey,
  values: TranslationValues = {},
): string {
  const template = MESSAGE_CATALOGS[locale][key] ?? MESSAGE_CATALOGS.en[key]
  return template.replace(/\{([A-Za-z0-9_]+)\}/gu, (placeholder, name: string) =>
    Object.hasOwn(values, name) ? String(values[name]) : placeholder,
  )
}
